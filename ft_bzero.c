#include "libft.h"

void	ft_bzero(char *s, int n)
{
	int	i;

	if (!s)
		return (NULL);
	i = 0;
	while (i < n)
	{
		*(unsigned char *)(str + i) = 0;
		i++;
	}
	return (0);
}
